const $ = (s) => document.querySelector(s);
const form = $("#auditForm");
const urlInput = $("#urlInput");
const runBtn = $("#runBtn");
const loading = $("#loading");
const loadingMsg = $("#loadingMsg");
const results = $("#results");
const errorBox = $("#errorBox");

const esc = (s) =>
  String(s ?? "").replace(/[&<>"]/g, (c) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;" }[c]));

const SEV_ORDER = ["critical", "high", "medium", "low"];

// Reflect whether Claude is wired up.
fetch("/api/health").then((r) => r.json()).then((h) => {
  const tag = $("#claudeTag");
  if (h.claude) { tag.textContent = "Claude fixes: ON"; tag.classList.add("on"); }
  else { tag.textContent = "Claude fixes: OFF"; }
}).catch(() => {});

async function runAudit(url) {

  errorBox.style.display = "none";
  results.style.display = "none";
  loading.style.display = "block";
  loadingMsg.textContent = "Fetching page & running auditors…";
  runBtn.disabled = true;

  // little staged message so long Claude calls feel alive
  const t = setTimeout(() => (loadingMsg.textContent = "Prioritizing findings & writing fixes…"), 1800);

  try {
    const res = await fetch("/api/audit", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ url }),
    });
    const data = await res.json();
    if (!res.ok) throw new Error(data.error || "Audit failed.");
    render(data);
  } catch (err) {
    errorBox.textContent = err.message;
    errorBox.style.display = "block";
  } finally {
    clearTimeout(t);
    loading.style.display = "none";
    runBtn.disabled = false;
  }
}

form.addEventListener("submit", (e) => {
  e.preventDefault();
  const url = urlInput.value.trim();
  if (url) runAudit(url);
});

// Allow deep-linking / hand-off from the marketing page: /?url=example.com
const preset = new URLSearchParams(location.search).get("url");
if (preset) { urlInput.value = preset; runAudit(preset); }

function render(d) {
  const m = d.meta;
  const counts = d.counts;

  const gaugeColor = d.score >= 80 ? "#138A4A" : d.score >= 65 ? "#F8CF3E" : d.score >= 50 ? "#FF5B2E" : "#C0341A";
  const r = 56, circ = 2 * Math.PI * r, off = circ * (1 - d.score / 100);

  const countPills = SEV_ORDER.concat("good")
    .filter((s) => counts[s] > 0)
    .map((s) => `<span class="pill ${s}">${counts[s]} ${s}</span>`)
    .join("");

  const cats = Object.values(d.byCategory)
    .map((c) => {
      const total = c.issues + c.wins || 1;
      const pct = Math.round((c.wins / total) * 100);
      return `<div class="cat">
        <div class="name">${esc(c.label)}</div>
        <div class="bar"><span style="width:${pct}%"></span></div>
        <div class="sub">${c.issues} issue${c.issues === 1 ? "" : "s"} · ${c.wins} ok</div>
      </div>`;
    }).join("");

  const issues = d.issues.map((i) => `
    <div class="issue">
      <div class="sev ${i.severity}"></div>
      <div>
        <div class="tags"><span class="tag">${esc(i.severityLabel)}</span><span class="tag">${esc(i.agent)}</span></div>
        <h4>${esc(i.title)}</h4>
        ${i.detail ? `<p>${esc(i.detail)}</p>` : ""}
        ${i.evidence ? `<div class="evidence">${esc(i.evidence)}</div>` : ""}
      </div>
    </div>`).join("");

  const sol = d.solutions || { fixes: [] };
  const fixNote = sol.engine === "rule-based"
    ? `<div class="fix-note">${esc(sol.note || "Deterministic fixes — set ANTHROPIC_API_KEY for AI-written ones.")}</div>`
    : sol.engine === "claude"
    ? `<div class="fix-note">Generated by Claude (${esc(sol.model || "")}). Review before shipping.</div>`
    : "";
  const fixActions = sol.downloadScript
    ? `<div class="fix-actions">
         <button class="btn-download" id="downloadFix">⬇  Download fix script</button>
         <span class="fix-actions-help">Run <code>node autoseo-fix.mjs your-file.html</code> on your machine to apply all fixes at once.</span>
       </div>`
    : "";

  const auto = d.automatic;
  const autoPanel = auto
    ? `
    <div class="auto-panel">
      <div class="auto-head">
        <h3 class="section-title" style="margin:0">⚡ Make it automatic</h3>
        <span class="auto-tag">always-on</span>
      </div>
      <p class="auto-lead">Embed one line in your site. We re-audit and refresh your SEO/GEO tags in the background — your site stays optimized without redeploys.</p>
      <div class="auto-snippet-row">
        <pre class="auto-snippet" id="autoSnippet">${esc(auto.snippet)}</pre>
        <button class="copy auto-copy" id="copyAutoSnippet">Copy</button>
      </div>
      <div class="auto-meta">
        <div class="auto-key"><span class="auto-k-label">API key</span><code>${esc(auto.apiKey)}</code></div>
        <div class="auto-actions">
          <button class="btn-download" id="downloadInstall">⬇  Download installer</button>
          <span class="auto-help"><code>node autoseo-install.mjs your-file.html</code> drops the snippet into your HTML for you.</span>
        </div>
      </div>
      <div class="auto-tip">Tip: the embedded agent updates tags client-side, which most search engines now render. For maximum SEO weight you can also commit the one-shot fixes below into your source.</div>
    </div>`
    : "";
  const fixes = (sol.fixes || []).map((fx, idx) => `
    <div class="fix">
      <div class="head"><span class="type">${esc(fx.type)}</span>
        <button class="copy" data-i="${idx}">Copy</button></div>
      <pre id="fix-${idx}">${esc(fx.value)}</pre>
      ${fx.rationale ? `<div class="why">${esc(fx.rationale)}</div>` : ""}
    </div>`).join("");

  results.innerHTML = `
    <div class="score-card">
      <div class="gauge">
        <svg width="128" height="128">
          <circle cx="64" cy="64" r="${r}" fill="none" stroke="#E8E1D2" stroke-width="12"/>
          <circle cx="64" cy="64" r="${r}" fill="none" stroke="${gaugeColor}" stroke-width="12"
            stroke-linecap="round" stroke-dasharray="${circ}" stroke-dashoffset="${off}"/>
        </svg>
        <div class="val"><div class="num">${d.score}</div><div class="grade">GRADE ${esc(d.grade)}</div></div>
      </div>
      <div class="score-meta">
        <h2>${esc(m.title || "(no title)")}</h2>
        <div class="url">${esc(m.finalUrl)}</div>
        <div class="facts">
          <span class="fact"><b>${m.status}</b> status</span>
          <span class="fact"><b>${m.https ? "HTTPS" : "HTTP"}</b></span>
          <span class="fact"><b>${m.redirects}</b> redirect${m.redirects === 1 ? "" : "s"}</span>
          <span class="fact"><b>${m.timeMs}</b> ms</span>
          <span class="fact"><b>${m.sizeKb}</b> KB</span>
        </div>
        <div class="counts">${countPills}</div>
      </div>
    </div>

    <h3 class="section-title">By category</h3>
    <div class="cats">${cats}</div>

    ${autoPanel}
    ${fixes ? `<h3 class="section-title">${auto ? "Or apply once now" : "Ready-to-paste fixes"}</h3>${fixNote}${fixActions}${fixes}` : ""}

    <h3 class="section-title">Findings (${d.issues.length})</h3>
    ${issues || `<p style="color:var(--ink-3)">No issues found — nicely optimized.</p>`}
  `;
  results.style.display = "block";

  results.querySelectorAll(".copy").forEach((btn) => {
    btn.addEventListener("click", () => {
      const pre = $("#fix-" + btn.dataset.i);
      navigator.clipboard.writeText(pre.textContent).then(() => {
        btn.textContent = "Copied ✓";
        setTimeout(() => (btn.textContent = "Copy"), 1500);
      });
    });
  });

  const dl = $("#downloadFix");
  if (dl && sol.downloadScript) {
    dl.addEventListener("click", () => {
      const blob = new Blob([sol.downloadScript], { type: "text/javascript" });
      const a = document.createElement("a");
      a.href = URL.createObjectURL(blob);
      a.download = "autoseo-fix.mjs";
      document.body.appendChild(a);
      a.click();
      a.remove();
      setTimeout(() => URL.revokeObjectURL(a.href), 1000);
      dl.textContent = "Downloaded ✓";
      setTimeout(() => (dl.textContent = "⬇  Download fix script"), 2000);
    });
  }

  const copyAuto = $("#copyAutoSnippet");
  if (copyAuto && auto) {
    copyAuto.addEventListener("click", () => {
      navigator.clipboard.writeText(auto.snippet).then(() => {
        copyAuto.textContent = "Copied ✓";
        setTimeout(() => (copyAuto.textContent = "Copy"), 1500);
      });
    });
  }

  const dlInstall = $("#downloadInstall");
  if (dlInstall && auto?.installScript) {
    dlInstall.addEventListener("click", () => {
      const blob = new Blob([auto.installScript], { type: "text/javascript" });
      const a = document.createElement("a");
      a.href = URL.createObjectURL(blob);
      a.download = "autoseo-install.mjs";
      document.body.appendChild(a);
      a.click();
      a.remove();
      setTimeout(() => URL.revokeObjectURL(a.href), 1000);
      dlInstall.textContent = "Downloaded ✓";
      setTimeout(() => (dlInstall.textContent = "⬇  Download installer"), 2000);
    });
  }

  results.scrollIntoView({ behavior: "smooth", block: "start" });
}
